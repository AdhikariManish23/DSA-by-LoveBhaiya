using namespace std;
    int main(){
    /* this is even number */
    int n = 20;
    int i =2;
    while( i<=n ){
        cout<<i <<endl;
        i+=2;
        /*This line increments the value of i by 2.
         Since you're only interested in even numbers, 
         you increase i by 2 in each iteration to move to the next even number.*/
    }

   
   /* this is odd number */
   
   
    int n = 10;
    int i = 1;
    while ( i<=n){
        cout<<i<<endl;
        i+=2;
    }

    
}